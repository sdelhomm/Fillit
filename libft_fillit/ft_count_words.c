#include "../fillit.h"

int		ft_count_words(char const *s, char c)
{
	int		i;
	int		n;

	n = 0;
	i = 0;
	while (s[i])
	{
		while (s[i] == c && s[i] != '\0')
			i++;
		if ((i == 0 && s[i] != c) || (s[i] != c && s[i - 1] == c))
			n++;
		i++;
	}
	return (n);
}
